<?php

echo "<u>Data Types :</u>";
echo "<br>";
/* 1. Integer
   - Represents whole numbers without a decimal point. */
   $integerVar = 42;   
    echo var_dump($integerVar) ."<br>"."<br>";
/* 2. Float 
   - Represents numbers with a decimal point. */
   	$floatVar = 3.14;
      echo var_dump($floatVar)."<br>"."<br>";
/* 3. String
   - Represents a sequence of characters. */
    $stringVar = "Hello, PHP!";
    echo var_dump($stringVar)."<br>"."<br>";
/* 4. Boolean
   - Represents either `true` or `false` */
   	$boolVar = true;
      echo var_dump($boolVar)."<br>"."<br>";
/* 5. Array
   - Represents an ordered map that can hold multiple values. */
   	$arrayVar = array(1, 2, 3, "PHP");
      echo var_dump($arrayVar)."<br>"."<br>";
/* 6. NULL
   - Represents the absence of a value or a variable without a value. */
   	$nullVar = null;
      echo var_dump($nullVar)."<br>"."<br>";


